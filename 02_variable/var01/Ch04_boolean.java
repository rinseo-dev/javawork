public class Ch04_boolean {

	public static void main(String[] args) {
		boolean bool1 = true;
		boolean bool2 = false;
		System.out.println(bool1);
		
		boolean bool3 = 10 < 3;
		System.out.println(bool3);
		
		int num1 = 10;
		int num2 = 3;
		boolean bool4 = num1 < num2;
		System.out.println(bool4);
	}
}
